const generateRandomNumber = () => {
  // GENERAR UN NUMERO ALEATORIO ENTRE 100000 y 999999
  //PISO DE 100000
  // DEL RANGO DESDE 0 A CASI 1 MULTIMPLICADO POR 900000 ==> Y SUMANDO 100000(DESPLAZAMIENTO)
  // FLOOR REDONDEA HACIA EL "PISO" NUMERO MAS PEQUEÃ‘O ==> CON ROUND PODRIA OBTENER 1.000.000 LO CUAL GENERARIA UN DIGITO MAS
  return Math.floor(100000 + Math.random() * 900000);
};

export default generateRandomNumber;
